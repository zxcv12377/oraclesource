-- 부서가 위치한 지역의 국가 ID 및 국가명을 조회한다
-- LOCATION, DEPARMENTS, COUNTRIES 테이블 사용

SELECT C.COUNTRY_ID, C.COUNTRY_NAME, D.DEPARTMENT_ID
FROM
	DEPARTMENTS d
JOIN LOCATIONS l ON
	L.LOCATION_ID = D.LOCATION_ID
JOIN COUNTRIES c ON
	L.COUNTRY_ID = C.COUNTRY_ID;

