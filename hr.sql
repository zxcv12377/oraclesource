-- 직업 ID가 SA_MAN 인 사원들의 최대 연봉보다 높게 받는 사원들의
-- LAST_NAME, JOB_ID, SALARY 조회
SELECT E.LAST_NAME, E.JOB_ID, E.SALARY
FROM EMPLOYEES e 
WHERE E.SALARY >
(SELECT MAX(E.SALARY) FROM EMPLOYEES e WHERE E.JOB_ID = 'SA_MAN');


-- COMMISION을 받는 사원들의 부서와 연봉이 동일한 사원들의
-- LAST_NAME, DEPTNO, SALARY 조회
SELECT
	E.LAST_NAME,
	E.DEPARTMENT_ID,
	E.SALARY
FROM
	EMPLOYEES e
WHERE
	E.DEPARTMENT_ID = ANY(
	SELECT
		E.DEPARTMENT_ID
	FROM
		EMPLOYEES e
	WHERE
		e.COMMISSION_PCT IS NOT NULL);


-- 회사 전체 평균 연봉보다 더 버는 사원들 중 LAST_NAME에 U가 있는 사원들이 근무하는 부서에서 근무하는 사원들의
-- EMPNO, LAST_NAME, SALARY조회

SELECT  E.EMPLOYEE_ID, E.LAST_NAME E, E.SALARY
FROM EMPLOYEES E
WHERE E.DEPARTMENT_ID = ANY (SELECT DISTINCT E.DEPARTMENT_ID
FROM EMPLOYEES e 
WHERE E.LAST_NAME LIKE ('%u%') AND E.SALARY > (SELECT AVG(E.SALARY)
FROM EMPLOYEES e) );


-- 각 부서별 평균 연봉보다 더 받는 동일부서 사원들의 
-- LAST_NAME, SALARY, DEPTNO, 해당 부서의 평균 연봉 조회(부서별 평균연봉을 기준으로 오름차순)

SELECT
	E.LAST_NAME
FROM
	EMPLOYEES e
WHERE
	(
	SELECT
		E.SALARY
	FROM
		EMPLOYEES e
	WHERE
		> (
		SELECT
			AVG(E.SALARY)
		FROM
			EMPLOYEES e
		GROUP BY
			E.DEPARTMENT_ID))
ORDER BY
	E.DEPARTMENT_ID;


